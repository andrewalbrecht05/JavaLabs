/**
 * Інтерфейс, який представляє музичний інструмент.
 */
public interface IMInstrument {

    /**
     * Метод, що відтворює звук музичного інструменту.
     * Реалізація цього методу повинна виводити повідомлення про те,
     * що інструмент грає або виконує які-небудь інші відповідні дії.
     */
    void play();
}